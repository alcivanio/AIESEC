import UIKit

extension UIColor {
    
    static var AIESECBlue: UIColor {
        get { return UIColor(red: 0, green: 155/255, blue: 1, alpha: 1.0) }
    }
    
    
    static var AIESECGray: UIColor {
        get { return UIColor(red: 51/255, green: 51/255, blue: 51/255, alpha: 1.0) }
    }
    
}
